// What is the purpose of the break and continue keywords in loops?

//break: The break statement is used to exit a loop prematurely. 
//When break is encountered, the loop stops executing, and control is transferred to the statement following the loop.

//and 

//continue: The continue statement is used to skip the current iteration of a loop and move to the next iteration. 
//When continue is encountered, the remaining code in the loop for that iteration is skipped.